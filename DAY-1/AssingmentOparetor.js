//2 Assingment Operators (for assign or update a value of variable)

// simple assignment
let name = 'Selina';
let age = 25;
let updatedAge = age + 5;

console.log("Your age: ", age) // Your age:  25
console.log('Now your age: ', updatedAge) // Now your age:  30


// addition operator (add ang assign)
let a = 10;
let b = 3;
let sum = a + b;
console.log('The sum of a & b is :', sum) // The sum of a & b is : 13


let x = 5;
x += 3;    // x = x + 5
console.log('The value of x is: ', x) // The value of x is:  8

// subtract & assign

let q = 10;
q -= 3;  // x = x - 3
console.log(q)  // 7


// multiply & assign

let w = 40;
w *= 3;  // w = w * 3
console.log(w)  // 120



// divide & assign

let e = 30;
e /= 10  // e = e / 10
console.log(e)  // 3


// modulus & assign

let r = 10;
r %= 3;  r = r % 3
console.log(r)  // 1


// exponentiation & assign

let y = 4
y **= 4; y = y ** 4
console.log(y)  // 256