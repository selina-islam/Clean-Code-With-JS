// 1. Arithmetic operator

let a = 10;
let b = 3;
let c = 5;

//Addition (add two or more number)
const sum = a + b;
console.log("Sum of (a + b)  =" ,sum) // Sum of (a + b)  = 13


// Subtraction (subtract two or more number)
const difference = a - b;
console.log('Subtraction: ', difference) // Subtraction:  7


// Multiplication ( multiply two or more number)
const product = a * b;
console.log("Multiplication : ", product)  // Multiplication :  30


// Division (divided two number)

const result = a / c // (10/5)
console.log(result) // 2

//Modulus (remainder)

const remainder = a % b;
console.log(remainder)  // 1


// Exponentiation (10 ^ 3)

const num1 = 10;
const num2 = 3;

const exponentiation = num1 ** num2;  // 10^3
console.log(exponentiation)  // 1000
