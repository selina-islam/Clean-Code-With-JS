// Basic Number Operations: addition, subtraction, multiplication, division

// 1.addition (+)

let a = 10, b = 5;
let sum = a + b;
console.log(`The sum of ${a} & ${b} is: ${sum}`)  // The sum of 10 & 5 is: 15

// practical example
let product1 = 20.99;
let product2 = 10.50;
let totalPrice = product1 + product2;
console.log("Total price:", totalPrice.toFixed(2))  // Total price: 31.49


// subtraction (-)
let subtraction = a - b;
console.log('Subtraction:', subtraction)  // Subtraction: 5

// practical example of subtraction
let budget = 100;
let expense = 45.25;
let remining= budget- expense
console.log('The remaining amount is:', remining)

//The remaining amount is: 54.75
 
// multiplication (*)
let product = a * b;
console.log('The product is:', product)  // The product is: 50

// practical example
let length = 5; // in meters
let width = 3; // in meters
let area = length * width;
console.log(`Area of the rectangle is: ${area} square meters`)  // Area of the rectangle is: 15 square meters


// division (/)

let quotient = a / b;
console.log('The quotient is: ', quotient)  // The quotient is:  2

// practical example of division

let totalBil = 120;
let numberOfFriends = 4;
let share = totalBil/ numberOfFriends
console.log(`Each friends pays: $${share}`) // Each friends pays: $30


// combining operations

let x = 10, y = 5, z = 2;
const result = (x + y) * z - (y / z);
console.log(result)  // 17.5

